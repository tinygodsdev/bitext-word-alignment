	<h2 id="seo-sections" class="sr-only">Guides</h2>
	<h3>What is word-by-word translation?</h3>
	<p>
		Word-by-word translation lines up each source word with the part of the translation that
		corresponds to it. Unlike a free translation, this view makes morphemes and multi-word units
		visible — especially when you add an <strong>interlinear gloss</strong> row.
	</p>
	<h3>How to align words between languages</h3>
	<p>
		Type your <strong>source sentence</strong> and <strong>target sentence</strong>, tap words on
		each side to select them, then create a link. The tool draws connector lines in the preview. You
		can use a <strong>bilingual sentence alignment tool</strong> like this when textbook examples need
		to be legible at a glance.
	</p>
	<h3>Interlinear gloss explanation</h3>
	<p>
		A gloss is a short label (often grammatical or lexical) under each word. Turn on gloss in
		settings to show a dedicated row between the two sentence lines — ideal for an
		<strong>interlinear gloss tool</strong> workflow.
	</p>
</section>
